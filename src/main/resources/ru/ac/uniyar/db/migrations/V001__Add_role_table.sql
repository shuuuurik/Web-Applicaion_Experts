CREATE TABLE ROLE (
      ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      NAME VARCHAR(50) NOT NULL,
      LIST_SPECIALISTS BOOLEAN NOT NULL,
      SHOW_SPECIALIST BOOLEAN NOT NULL,
      EDIT_USER BOOLEAN NOT NULL,
      LIST_REQUESTS BOOLEAN NOT NULL,
      SHOW_REQUEST BOOLEAN NOT NULL,
      ADD_REQUEST BOOLEAN NOT NULL,
      EDIT_REQUEST BOOLEAN NOT NULL,
      ADD_ANNOUNCEMENT BOOLEAN NOT NULL,
      EDIT_ANNOUNCEMENT BOOLEAN NOT NULL,
      ADD_CITY BOOLEAN NOT NULL,
      ADD_CATEGORY BOOLEAN NOT NULL
);