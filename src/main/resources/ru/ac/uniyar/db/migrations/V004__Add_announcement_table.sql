CREATE TABLE ANNOUNCEMENT (
   ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   ADDING_TIME DATETIME NOT NULL,
   CATEGORY_NAME VARCHAR(30) NOT NULL,
   TITLE VARCHAR(30) NOT NULL,
   CITY_NAME VARCHAR(30) NOT NULL,
   DESCRIPTION CHARACTER LARGE OBJECT NOT NULL,
   SPECIALIST_ID INT NOT NULL,
   CONSTRAINT FK_ANNOUNCEMENT_CITY FOREIGN KEY (CITY_NAME) REFERENCES CITY(NAME),
   CONSTRAINT FK_ANNOUNCEMENT_CATEGORY FOREIGN KEY (CATEGORY_NAME) REFERENCES CATEGORY(NAME),
   CONSTRAINT FK_ANNOUNCEMENT_SPECIALIST FOREIGN KEY (SPECIALIST_ID) REFERENCES SPECIALIST(ID)
);
