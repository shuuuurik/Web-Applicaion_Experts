CREATE TABLE REQUEST (
  ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  USERS_USERNAME VARCHAR(50) NOT NULL,
  CATEGORY_ID INT NOT NULL,
  EDUCATION CHARACTER LARGE OBJECT NOT NULL,
  EXPERIENCE CHARACTER LARGE OBJECT NOT NULL,
  STATUS VARCHAR(20) NOT NULL,
  REASON CHARACTER LARGE OBJECT,
  ADDING_TIME DATETIME NOT NULL,
  CONSTRAINT FK_REQUEST_USERS FOREIGN KEY (USERS_USERNAME) REFERENCES USERS(USERNAME),
  CONSTRAINT FK_REQUEST_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORY(ID)
);