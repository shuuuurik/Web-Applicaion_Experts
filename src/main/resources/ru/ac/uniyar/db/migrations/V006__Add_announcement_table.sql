CREATE TABLE ANNOUNCEMENT (
   ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   CATEGORY_ID INT NOT NULL,
   CITY_NAME VARCHAR(30) NOT NULL,
   TITLE VARCHAR(30) NOT NULL,
   DESCRIPTION CHARACTER LARGE OBJECT NOT NULL,
   ADDING_TIME DATETIME NOT NULL,
   USERS_USERNAME VARCHAR(50) NOT NULL,
   CONSTRAINT FK_ANNOUNCEMENT_CITY FOREIGN KEY (CITY_NAME) REFERENCES CITY(NAME),
   CONSTRAINT FK_ANNOUNCEMENT_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORY(ID),
   CONSTRAINT FK_ANNOUNCEMENT_USERS FOREIGN KEY (USERS_USERNAME) REFERENCES USERS(USERNAME)
);
